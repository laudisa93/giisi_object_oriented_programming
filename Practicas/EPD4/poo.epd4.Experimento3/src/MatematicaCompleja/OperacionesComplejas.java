package MatematicaCompleja;

/**
 *
 * @author usuarioupo
 */
public class OperacionesComplejas {

    public static double seno(double a) {
        return Math.sin(a);
    }

    public static double coseno(double a) {
        return Math.cos(a);
    }

    public static double raizCuadrada(double a) {
        return Math.sqrt(a);
    }

    public static double inverso(double a) {
        return 1/a;
    }
}
